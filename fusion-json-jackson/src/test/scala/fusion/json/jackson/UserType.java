package fusion.json.jackson;

import com.fasterxml.jackson.annotation.JsonValue;
import helloscala.common.data.IntEnum;

public enum UserType implements IntEnum {
    NORMAL(1, "NORMAL"),
    ADMIN(2, "ADMIN");

    @JsonValue
    private Integer value;
    private String name;

    UserType(int value, String name) {
        this.value = value;
        this.name = name;
    }

    @Override
    public Integer getValue() {
        return value;
    }

    @Override
    public String getName() {
        return name;
    }
}
